#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*content;
	t_list	*node;

	if (!lst)
		return (NULL);
	new = malloc (sizeof (t_list));
	if (!new)
		return (NULL);
	while (lst)
	{
		content = f(lst->content);
		node = ft_lstnew(content);
		if (!node)
		{
			del(content);
			ft_lstclear(&new, del);
			return (NULL);
		}
		ft_lstadd_back(&lst, node);
		lst = lst->content;
	}
	return (new);
}
